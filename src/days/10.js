const validPairs = [
  ['(', ')'],
  ['[', ']'],
  ['{', '}'],
  ['<', '>'],
];

const points = {
  ')': 3,
  ']': 57,
  '}': 1197,
  '>': 25137,
};

const points2 = {
  ')': 1,
  ']': 2,
  '}': 3,
  '>': 4,
};

const openers = validPairs.map(pair => pair[0]);
const closers = validPairs.reduce((acc, [open, close]) => ({ ...acc, [open]: close }), {});

const loadChars = (str) => {
  const rows = str.split('\n').map(row => row.split(''));
  const stats = rows.reduce(
    (acc, row) => {
      const waitingFor = [];
      let invalidChar = null;
      const loadChar = (char) => {
        if (!waitingFor.length) {
          waitingFor.unshift(closers[char]);
        } else if (openers.includes(char)) {
          waitingFor.unshift(closers[char]);
        } else if (waitingFor[0] === char) {
          waitingFor.shift();
        } else {
          invalidChar = char;
        }
      };
      row.forEach(char => !invalidChar && loadChar(char));
      const out = { ...acc };
      if (invalidChar) {
        out.invalidChars.push(invalidChar);
      } else {
        out.waitingFor.push(waitingFor);
      }
      return out;
    },
    { invalidChars: [], waitingFor: [] },
  );
  return {
    invalidChars: stats.invalidChars,
    waitingFor: stats.waitingFor,
  };
};

const load1 = (str) => loadChars(str)
  .invalidChars
  .reduce((acc, char) => acc + points[char], 0);

const getScore2 = (vals) => vals.reduce((acc, char) => (acc * 5) + points2[char], 0);
const load2 = (str) => {
  const waitingScores = loadChars(str)
    .waitingFor
    .reduce((acc, vals) => ([...acc, getScore2(vals)]), [])
    .sort((a, b) => a - b);
  return waitingScores[Math.floor(waitingScores.length / 2)];
};

export default {
  loadTest: () => load1(testData),
  loadData: () => load1(data),
  loadTest2: () => load2(testData),
  loadData2: () => load2(data),
};
